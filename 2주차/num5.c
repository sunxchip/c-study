#include <stdio.h>

int main(){
    printf("The double quotation mark is \".\n");
    printf("The singe quotation mark is \'.\n");
    printf("Character A is \'A\'.\n");
    printf("To represent a double quotation mark, use the escape sequence \\\".\n");

    return 0;
}

// The double quotation mark is ".
// The singe quotation mark is '.
// Character A is 'A'.
// To represent a double quotation mark, use the escape sequence \".